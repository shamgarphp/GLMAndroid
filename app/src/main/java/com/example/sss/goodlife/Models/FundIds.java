package com.example.sss.goodlife.Models;

public class FundIds {
    String  fund_id,
            program_name,
            allocate_fund,
            advance_issued,
            apply_for_advance,
            description,
            created_on;

    public String getFund_id() {
        return fund_id;
    }

    public void setFund_id(String fund_id) {
        this.fund_id = fund_id;
    }

    public String getProgram_name() {
        return program_name;
    }

    public void setProgram_name(String program_name) {
        this.program_name = program_name;
    }

    public String getAllocate_fund() {
        return allocate_fund;
    }

    public void setAllocate_fund(String allocate_fund) {
        this.allocate_fund = allocate_fund;
    }

    public String getAdvance_issued() {
        return advance_issued;
    }

    public void setAdvance_issued(String advance_issued) {
        this.advance_issued = advance_issued;
    }

    public String getApply_for_advance() {
        return apply_for_advance;
    }

    public void setApply_for_advance(String apply_for_advance) {
        this.apply_for_advance = apply_for_advance;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCreated_on() {
        return created_on;
    }

    public void setCreated_on(String created_on) {
        this.created_on = created_on;
    }
}
